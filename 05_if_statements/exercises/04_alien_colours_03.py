# - If the alien’s color is green, print a statement that the player just earned 5 points for shooting the alien.
# - If the alien’s color is yellow, print a statement that the player just earned 10 points.
# - If the alien’s color is red, print a statement that the player just earned 15 points.
# - Write three versions of this program, making sure each message is printed for the appropriate color alien.

alien_color = 'green'

if alien_color == 'green':
    print("The player just earned 5 points.")

elif alien_color == 'yellow':
    print("The player just earned 10 points.")

else:
    print("The player just earned 15 points.")

alien_color = 'yellow'

if alien_color == 'green':
    print("The player just earned 5 points.")

